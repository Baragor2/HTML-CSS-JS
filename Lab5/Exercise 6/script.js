function square(num) {
    return num * num;
}


function sum_nums(num1, num2) {
    return num1 + num2;
}


function dif_and_div(num1, num2, num3) {
    return (num1 - num2) / num3;
}


function day_week(num) {
    week = ['Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота', 'Воскресенье'];
    return week[num - 1];
}